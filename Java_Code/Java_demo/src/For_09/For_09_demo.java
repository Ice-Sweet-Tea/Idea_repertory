package For_09;

/*
    for语句

    格式：
        for(初始化语句;条件判断语句;条件控制语句) {
            循环体语句;
        }
 */
public class For_09_demo {
    public static void main(String[] args) {
    //需求：在控制台输出5次"HelloWorld"
        for (int i = 1;i<=5;i++){
            System.out.println("HelloWorld!");
        }

    }
}
